    private void jBregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBregistrarActionPerformed
        String formato = "dd/MM/yyyy";
        if(jTcliente.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Campo Obrigatorio!");
            jTcliente.requestFocus();
        } else if (jTlivro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo obrigatório!");
            jTlivro.requestFocus();
        } else if (jTdataemprestimos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo obrigatório!");
            jTdataemprestimos.requestFocus();
        } else if (jTdatadevolucao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo obrigatório!");
            jTdatadevolucao.requestFocus();
        } else if (!validarFormatoData(jTdataemprestimos.getText(),formato)) { 
            JOptionPane.showMessageDialog(rootPane, "Formato de data inválido, tente dd/MM/yyyy!");
            jTdataemprestimos.requestFocus();
        } else if (!validarFormatoData(jTdatadevolucao.getText(),formato)) {
            JOptionPane.showMessageDialog(rootPane, "Formato de data inválido, tente dd/MM/yyyy!");
            jTdatadevolucao.requestFocus();
        }else{
            try {
                em.setId_leitor(Integer.parseInt(jTcliente.getText()));
                em.setId_livro(Integer.parseInt(jTlivro.getText()));

                em.setData_emprestimo(jTdataemprestimos.getText());
                em.setData_devolucao(jTdatadevolucao.getText());                
                
                if(jRsim.isSelected()){
                    em.setDevolucao("SIM");
                } else if(jRnao.isSelected()){
                    em.setDevolucao("NAO");
                }
                
                dao.inserir(em);
            } catch (Exception ex) {
                //Logger.getLogger(CadLivro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jBregistrarActionPerformed

